package myClassesYay;
public class Dog
{
    private String breed;
    private double size;
    private String furColor;

    //Accessors or Getters
    public String getbreed()
    {
        return breed;
    }
    public double getsize()
    {
        return size;
    }
    public String gtefurcolor()
    {
        return furColor;
    }
    //Mutators or setters
    public void setBreed(String theBreed)
    {
        breed = theBreed;
    }
    public void setColor (String theColor)
    {
        furColor = theColor;
    }
    public void setSize(double theSize)
    {
        size = theSize;
    }
}
